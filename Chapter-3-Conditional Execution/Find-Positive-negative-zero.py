# Find if the number is positive, negative or zero

num = int(input("Enter the number: ")) # input number

if num > 0:                             # check if the number is positive
    print("The number is positive")
elif num < 0:                           # check if the number is negative
    print("The number is negative")
else:                                   # check if the number is zero
    print("The number is zero")